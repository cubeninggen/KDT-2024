#------------------------------------------------------------------------------------------
# [실습] 문자 1개 코드값을 저장하는 조건식을 작성
# - 알파벳(a~z, A~Z) 코드값으로 변환
# - 그외에 코드값은 None으로 반환
#------------------------------------------------------------------------------------------
msg=input('알파벳만 입력 : ').strip()
print(f'{msg}의 코드값 : {list(map(ord,msg))}')if len(msg)>0 and 'a'<=msg<='z'or'A'<=msg<='Z'else print(f'{msg}의 코드값 : {None}')