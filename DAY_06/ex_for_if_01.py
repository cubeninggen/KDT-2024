## [실습]
## 메시지에서 알파벳과 숫자를 구분해서 처리 합니다.
## 알파벳은 ★, 숫자는 ♡로 변경해서 출력해주세요.
#------------------------------------------------------------------------------------------
msg=input('알파벳은 ★, 숫자는 ♡로 변경 : ').strip()
msg2=''
for i in msg:
    if'a'<=i<='z'or'A'<=i<='Z':
        print('★',end='')
        msg2=msg2+'★'
    elif'0'<=i<='9':
        print('♡',end='')
        msg2=msg2+'♡'
    else:
        print(i,end='')
        msg2=msg2+i
print()
print(f'msg2 => {msg2}')