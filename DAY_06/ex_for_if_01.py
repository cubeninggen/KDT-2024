## [실습]
## 숫자 1~50까지의 데이터가 있습니다.
## 해당 데이터에서 3의 배수는 제곱을 하고,
## 나머지 숫자는 그대로 해서 모두 더해서 합계를 출력하세요
#------------------------------------------------------------------------------------------
datas=range(1,51)
data=0
for i in datas:
    if i%3==0:
        data=data+(i**2)
    else:
        data=data+i
print(data)

