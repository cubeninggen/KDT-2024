## [실습] 3단 출력하기. 단, while문 사용
line=1
while line<=9:
    print(f'3*{line}={3*line}')
    line=line+1

## [실습] 1~30 범위의 숫 중에서 홀수만 출력 단, while문 사용
cnt=1
while cnt<=30:
    if cnt%2:
        print(cnt)
    cnt=cnt+1
while cnt<=30:
    print(cnt)
    cnt=cnt+2