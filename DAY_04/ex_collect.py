# unpacking 방식 : 변수1,변수2,...변수n = tuple1,tuple2,...tuple n <== 원소 갯수 중요!
name,age,job,gender='홍길동',12,'의적','남'
#---------------------------------------------------------------------------------------------

# name,age='홍길동',12,'의적','남'  <== ERROR!
print(name,age,job,gender)

name,age,_,_='홍길동',12,'의적','남' # <== 문법을 충족시키기위해 '_'사용 ('_'은 의미없는값에 자주사용!)
print(name,age,_) # <== '_'는 마지막값 출력

grade=[100,99]
