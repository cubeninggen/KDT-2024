#---------------------------------------------------------------------------------------------
# 생성자(Constructor) 함수 : 타입명과 동일한 이름의 함수
# - int(), float(), str(), bool(), list(), tuyple(), dict(), set(), map(), range()
#---------------------------------------------------------------------------------------------
# 기본 데이터 타입
num=int(10)       # num=10
fnum=float(10.2)  # float=10.2
msg=str('Good')   # msg='good'
isOk=bool()       # isOk=False
print(num,fnum,msg,isOk)

# 컬랙션 타입
lnums=list([1,2,3,4])  #lnums=[1,2,3,4]
tnums=tuple((3,6,9))   #tnums=(3,6,9)
ds=dict({'d1':10,'d2':30})   #ds={'d1':10,'d2':30}
ss=set({1,1,3,3,5})    #ss={1,1,3,3,5}
print(lnums,tnums,ds,ss)

# 타입 변경 => 형변환
# dict 자료형은 다른 자료형과 달리 데이터 형태 다름
# - 데이터 형태 => 키:값
# - dict(키1=값,키2=값,....)
ds=dict(n1=1,n2=2,n3='남') #이 방식에서 key값은 str만 가능하며 ''사용X
print(ds)

ds=dict([('name','마징가'),('age',12)]) # tuple로 받으면 (key,value)로 치환
print(ds)

ds=dict([['name','마징가'],['age',12]]) # list로 받으면 [key,value]로 치환
print(ds)

## 내장함수 : zip() <= 같은 인덱스의 요소끼리 묶어줌
l1=['name','age','gender']
l2=['마징가',12,'남']
l3=[False,True,True]
print(list(zip(l1,l2,l3)))

ds=dict(zip(l1,l2))
print(ds)
